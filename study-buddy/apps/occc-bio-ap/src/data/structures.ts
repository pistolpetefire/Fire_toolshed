export function getStructureById(id: string): Structure | undefined {
  return structures.find((s) => s.id === id);
}

export function getStructuresBySystem(systemId: string): Structure[] {
  return structures.filter((s) => s.systemId === systemId);
}

export function searchStructures(query: string): Structure[] {
  const q = query.trim().toLowerCase();
  if (!q) return structures;
  return structures.filter((s) => {
    const haystack = [
      s.name,
      s.category,
      s.function,
      s.systemId,
      ...(s.aliases ?? []),
      ...s.relations,
    ]
      .join(' ')
      .toLowerCase();
    return haystack.includes(q);
  });
}
